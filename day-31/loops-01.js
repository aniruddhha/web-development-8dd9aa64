
function simpleFor() {
    console.log(`游릭 For Loop 游릭`)
    for (let i = 0; i < 10; i++) {
        console.log(`Hi`)
    }
}

function whileLoop() {
    console.log(`游릭 While Loop 游릭`)
    let i = 0
    while ( i < 10 ) {
        console.log(`Hi`)
        i++
    }
}

function doWhile() {
    console.log(`游릭 Do While Loop 游릭`)
    let i = 0;
    do {
        console.log(`Hi`)
        i++
    } while( i < 10 )
}


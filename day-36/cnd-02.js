const day = 1

switch(day) {
    case 1: {
        console.log(`MON`)
        break
    }
    case 2: {
        console.log(`TUE`)
        break;
    }
    case 3: {
        console.log(`WED`)
        break;
    }
    case 4: {
        console.log(`THU`)
        break;
    }
    case 5: {
        console.log(`FRI`)
        break;
    }
    case 6: {
        console.log(`SAT`)
        break;
    }
    case 7: {
        console.log(`SUN`)
        break;
    }
    default: {
        console.log(`Invalid Day`)
    }
}